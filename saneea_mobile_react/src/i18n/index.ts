export function setupI18n(language: "ar" | "en" = "ar") {
  if (!i18n.isInitialized) {
    i18n.use(initReactI18next).init({
      resources,
      lng: language,
      fallbackLng: "ar",
      interpolation: { escapeValue: false },
      compatibilityJSON: "v4",
    });
  } else {
    i18n.changeLanguage(language);
  }
  return i18n;
}

